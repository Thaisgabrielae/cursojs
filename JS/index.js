// Laços de Repetição

// SEI QUANTAS VEZES QUERO EXECUTAR?
// SIM - UTILIZAR FOR
// NÃO - UTILIZAR WHILE


// FOR --- Para
for(let i = 0; i < 5; i++) {
    console.log(i);
}

// WHILE --- Enquanto
let i = 0;
while(i < 5) {
    console.log(i);
    i++;
}
// DO WHILE -- Faça enquanto

let i = 10;
do {
    console.log(i);
    i++;
} while(i < 5);
console.log('Fim do While');









